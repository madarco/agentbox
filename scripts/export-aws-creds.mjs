#!/usr/bin/env node
/**
 * Export the current AWS session into `aws_credentials/` for the box carry.
 *
 * The problem this solves: a box cannot use an SSO profile. SSO stores a bearer
 * token in `~/.aws/sso/cache` which the SDK exchanges for role credentials, and
 * only `aws sso login` — a browser flow — can refresh it. A box has no browser.
 *
 * The fix is to not copy the SSO token at all, but the credentials it *resolves
 * to*: `aws configure export-credentials` materializes the profile into a plain
 * access-key/secret/session-token triple. So the host keeps SSO (no IAM user, no
 * long-lived key anywhere), and the box gets an ordinary short-lived credential
 * file.
 *
 * Usage (host):
 *   aws sso login --profile agentboxmarco
 *   pnpm aws:creds                       # writes aws_credentials/{config,credentials}
 *   agentbox claude --provider docker    # the carry picks them up
 *
 * Override the profile with AWS_PROFILE, and the region with AWS_REGION.
 *
 * THESE CREDENTIALS EXPIRE — an SSO role session is typically 1 hour (up to 12,
 * if the permission set allows). They are baked into the box at CREATE time by
 * `carry:`, so a long-lived box will eventually see `ExpiredToken`. Re-run this
 * script and re-create the box (or `agentbox cp` the files in) to refresh.
 */

import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(repoRoot, 'aws_credentials');

const profile = process.env.AWS_PROFILE ?? 'agentboxmarco';
const region = process.env.AWS_REGION ?? 'us-east-1';

function fail(msg) {
  console.error(`export-aws-creds: ${msg}`);
  process.exit(1);
}

let exported;
try {
  // `export-credentials` resolves the profile through the full chain (SSO,
  // assume-role, static keys — it does not care which) and prints the concrete
  // credentials it ended up with. Requires AWS CLI v2.13+.
  const raw = execFileSync(
    'aws',
    ['configure', 'export-credentials', '--profile', profile, '--format', 'process'],
    { encoding: 'utf8', stdio: ['ignore', 'pipe', 'pipe'] },
  );
  exported = JSON.parse(raw);
} catch (err) {
  const stderr = String(err.stderr ?? err.message ?? '');
  if (err.code === 'ENOENT') {
    fail(
      'the AWS CLI is not installed on this host.\n' +
        '  This script runs on the HOST (it needs your SSO session), not inside a box.\n' +
        '  Install: https://docs.aws.amazon.com/cli/latest/userguide/getting-started-install.html',
    );
  }
  if (/sso|token|expired/i.test(stderr)) {
    fail(
      `the session for profile "${profile}" is not valid.\n` +
        `  Run:  aws sso login --profile ${profile}\n` +
        `  Then: pnpm aws:creds`,
    );
  }
  if (/could not be found|does not exist/i.test(stderr)) {
    fail(
      `profile "${profile}" is not in ~/.aws/config.\n` +
        '  Set AWS_PROFILE=<name> pnpm aws:creds, or add the profile first.',
    );
  }
  fail(`\`aws configure export-credentials\` failed:\n${stderr.trim()}`);
}

const { AccessKeyId, SecretAccessKey, SessionToken, Expiration } = exported;
if (!AccessKeyId || !SecretAccessKey) {
  fail(`profile "${profile}" resolved to no usable credentials`);
}

mkdirSync(outDir, { recursive: true, mode: 0o700 });

const credentials =
  `# Generated by \`pnpm aws:creds\` from profile "${profile}". DO NOT COMMIT.\n` +
  `# Carried into a box as ~/.aws/credentials by agentbox.yaml.\n` +
  `[default]\n` +
  `aws_access_key_id = ${AccessKeyId}\n` +
  `aws_secret_access_key = ${SecretAccessKey}\n` +
  (SessionToken ? `aws_session_token = ${SessionToken}\n` : '');

const config = `[default]\nregion = ${region}\n`;

writeFileSync(join(outDir, 'credentials'), credentials, { mode: 0o600 });
writeFileSync(join(outDir, 'config'), config, { mode: 0o600 });

const kind = SessionToken ? 'temporary (session token)' : 'static';
console.log(`export-aws-creds: wrote aws_credentials/{config,credentials}`);
console.log(`  profile: ${profile}  region: ${region}  credentials: ${kind}`);
if (Expiration) {
  const mins = Math.round((Date.parse(Expiration) - Date.now()) / 60000);
  console.log(`  expires: ${Expiration} (~${String(mins)} min) — re-run before creating a new box`);
}
